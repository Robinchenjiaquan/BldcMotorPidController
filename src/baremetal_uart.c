void BM_UART_Init(USART_TypeDef *UARTx, uint32_t bbr_value)
{
	UARTx->BRR = (uint16_t)(bbr_value);
	UARTx->CR1 = (uint16_t)((1 << 13)|(1 << 3)|(1 << 2));
}
